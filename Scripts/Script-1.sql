-- 3 .
 
SELECT PROFESSOR_NAME 교수이름,
FLOOR( MONTHS_BETWEEN( SYSDATE, TO_DATE('19' || SUBSTR( PROFESSOR_SSN, 1, 6), 'YYMMDD')) /12 ) 나이
FROM TB_PROFESSOR
ORDER BY 2;

