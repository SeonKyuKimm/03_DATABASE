SELECT STUDENT_NO, STUDENT_NAME
FROM TB_STUDENT
WHERE EXTRACT(YEAR FROM ENTRANCE_DATE) -
			EXTRACT(YEAR FROM TO_DATE('19' || SUBSTR(STUDENT_SSN, 1,6),'YYYYMMDD') ) > 19;

		
